func TestGREASEECHWrite(t *testing.T) {
	for _, testsuite := range []rawECHTestSuite{rawECH_HKDFSHA256_AES128GCM} {

		gech := &tls.GREASEEncryptedClientHelloExtension{}

		n, err := gech.Write(testsuite.raw[4:]) // skip the first 4 bytes which are the extension type and length
		if err != nil {
			t.Fatalf("Failed to write GREASE ECH extension: %s", err)
		}

		if n != len(testsuite.raw[4:]) {
			t.Fatalf("Failed to write all GREASE ECH extension bytes: %d != %d", n, len(testsuite.raw[4:]))
		}

		gechBytes := make([]byte, 1024)
		n, err = gech.Read(gechBytes)
		if err != nil && !errors.Is(err, io.EOF) {
			t.Fatalf("Failed to read GREASE ECH extension: %s", err)
		}

		if n != len(testsuite.raw) {
			t.Fatalf("GREASE ECH Read length mismatch: %d != %d", n, len(testsuite.raw))
		}

		// manually check fields in the GREASE ECH extension
		if len(gech.CandidateCipherSuites) != 1 ||
			gech.CandidateCipherSuites[0].KdfId != testsuite.kdfID ||
			gech.CandidateCipherSuites[0].AeadId != testsuite.aeadID {
			t.Fatalf("GREASE ECH Read cipher suite mismatch")
		}

		if len(gech.EncapsulatedKey) != int(testsuite.encapsulatedKeyLength) {
			t.Fatalf("GREASE ECH Read encapsulated key length mismatch")
		}

		// After Write(), the extension should preserve the exact wire format size.
		// The payload is now stored directly (not via CandidatePayloadLens) to ensure
		// fingerprint accuracy during round-trips. Verify that Len() matches the original.
		if gech.Len() != len(testsuite.raw) {
			t.Fatalf("GREASE ECH Len() mismatch: got %d, want %d", gech.Len(), len(testsuite.raw))
		}
	}
}

type rawECHTestSuite struct {
	kdfID                 uint16
	aeadID                uint16
	encapsulatedKeyLength uint16
	payloadLength         uint16

	raw []byte
}

func TestGREASEECHWriteValidation(t *testing.T) {
	tests := []struct {
		name    string
		raw     []byte
		wantErr string
	}{
		{
			name: "empty encapsulated key",
			raw: []byte{
				0x00,             // OuterClientHello
				0x00, 0x01,       // KDF: HKDF_SHA256
				0x00, 0x01,       // AEAD: AES_128_GCM
				0x42,             // config_id
				0x00, 0x00,       // enc length: 0 (empty)
				0x00, 0x20,       // payload length: 32
				0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08,
				0x09, 0x0a, 0x0b, 0x0c, 0x0d, 0x0e, 0x0f, 0x10,
				0x11, 0x12, 0x13, 0x14, 0x15, 0x16, 0x17, 0x18,
				0x19, 0x1a, 0x1b, 0x1c, 0x1d, 0x1e, 0x1f, 0x20,
			},
			wantErr: "empty encapsulated key",
		},
		{
			name: "empty payload",
			raw: []byte{
				0x00,             // OuterClientHello
				0x00, 0x01,       // KDF: HKDF_SHA256
				0x00, 0x01,       // AEAD: AES_128_GCM
				0x42,             // config_id
				0x00, 0x20,       // enc length: 32
				0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08,
				0x09, 0x0a, 0x0b, 0x0c, 0x0d, 0x0e, 0x0f, 0x10,
				0x11, 0x12, 0x13, 0x14, 0x15, 0x16, 0x17, 0x18,
				0x19, 0x1a, 0x1b, 0x1c, 0x1d, 0x1e, 0x1f, 0x20,
				0x00, 0x00,       // payload length: 0 (empty)
			},
			wantErr: "empty ECH payload",
		},
		{
			name: "payload too short for AEAD overhead",
			raw: []byte{
				0x00,             // OuterClientHello
				0x00, 0x01,       // KDF: HKDF_SHA256
				0x00, 0x01,       // AEAD: AES_128_GCM
				0x42,             // config_id
				0x00, 0x20,       // enc length: 32
				0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08,
				0x09, 0x0a, 0x0b, 0x0c, 0x0d, 0x0e, 0x0f, 0x10,
				0x11, 0x12, 0x13, 0x14, 0x15, 0x16, 0x17, 0x18,
				0x19, 0x1a, 0x1b, 0x1c, 0x1d, 0x1e, 0x1f, 0x20,
				0x00, 0x10,       // payload length: 16 (only AEAD overhead, no plaintext)
				0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08,
				0x09, 0x0a, 0x0b, 0x0c, 0x0d, 0x0e, 0x0f, 0x10,
			},
			wantErr: "payload too short for AEAD overhead",
		},
	}

	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			gech := &tls.GREASEEncryptedClientHelloExtension{}
			_, err := gech.Write(tt.raw)
			if err == nil {
				t.Fatalf("expected error containing %q, got nil", tt.wantErr)
			}
			if !strings.Contains(err.Error(), tt.wantErr) {
				t.Fatalf("expected error containing %q, got %q", tt.wantErr, err.Error())
			}
		})
	}
}

