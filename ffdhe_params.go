// ffdheGroupParams contains the prime modulus for each FFDHE group.
// All groups use generator g=2.
type ffdheGroupParams struct {
	p    *big.Int // Prime modulus
	size int      // Key size in bytes
}

// ffdheGroups maps CurveID to FFDHE group parameters.
// Uses CurveFFDHE* constants for full FFDHE key exchange support.
var ffdheGroups = map[CurveID]*ffdheGroupParams{
	CurveFFDHE2048: {p: ffdhe2048P, size: 256},  // 2048 bits = 256 bytes
	CurveFFDHE3072: {p: ffdhe3072P, size: 384},  // 3072 bits = 384 bytes
	CurveFFDHE4096: {p: ffdhe4096P, size: 512},  // 4096 bits = 512 bytes
	CurveFFDHE6144: {p: ffdhe6144P, size: 768},  // 6144 bits = 768 bytes
	CurveFFDHE8192: {p: ffdhe8192P, size: 1024}, // 8192 bits = 1024 bytes
}

// mustParseBigIntHex parses a hex string into a big.Int, panicking on error.
// This is only used for compile-time constants.
func mustParseBigIntHex(s string) *big.Int {
	n, ok := new(big.Int).SetString(s, 16)
	if !ok {
		panic("invalid hex string for big.Int: " + s)
	}
	return n
}

// getFFDHEGroupParams returns the FFDHE group parameters for the given CurveID.
// Returns nil if the CurveID is not an FFDHE group.
func getFFDHEGroupParams(group CurveID) *ffdheGroupParams {
	return ffdheGroups[group]
}
